            'amount' => 'Кол-во товара',
            'sum' => 'Сумма заказа',
            'status' => 'Статус',
            'name' => 'Имя',
            'email' => 'Email',
            'phone' => 'Телефон',
            'address' => 'Адрес',
        ];
    }
}
